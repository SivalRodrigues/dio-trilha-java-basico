public class ExemploFor {
    public static void main(String[] args) {
        
        for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos ++){
            System.out.println("Contando Carneirinhos " + carneirinhos);

            System.out.println("Joãozinho Dormiu");

            /*int carneirinhos = 1: Inicializa a variável carneirinhos com o valor 1. Esta variável controla o número de vezes que o laço será executado.

            carneirinhos <= 20: Condição de execução. O laço continuará enquanto carneirinhos for menor ou igual a 20.

            carneirinhos++: Incrementa o valor da variável carneirinhos em 1 a cada iteração.
            
            DECALRO / CONDIÇÃO / INCREMENTA   
            
            */
        }
    }
    
}
